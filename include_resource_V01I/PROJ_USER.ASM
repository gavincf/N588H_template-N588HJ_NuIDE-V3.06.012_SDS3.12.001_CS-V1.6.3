

;==========================
BK0_STORE_BPX:
.IF ( C_BP0_STATUS_EN .EQ. 1 )
	LDA	!BP0
	STA	BP0_STATUS
.ENDIF
.IF ( C_BP1_STATUS_EN .EQ. 1 )
	LDA	!BP1
	STA	BP1_STATUS
.ENDIF
.IF ( C_BP2_STATUS_EN .EQ. 1 )
	LDA	!BP2
	STA	BP2_STATUS
.ENDIF
;
	RTS
;==========================

BK0_STORE_BPX_WO_BOND_PWOFF:
.IF ( C_BP0_STATUS_EN .EQ. 1 )
	LDA	BP0_STATUS
	AND	#C_BP0_BOND
	STA	BP0_STATUS
	LDA	#C_BP0_BOND
	EOR	#0FFH
	AND	!BP0 ;READ
	ORA	BP0_STATUS
	STA	BP0_STATUS
.ENDIF
.IF ( C_BP1_STATUS_EN .EQ. 1 )
	LDA	BP1_STATUS
	AND	#C_BP1_BOND
	STA	BP1_STATUS
	LDA	#C_BP1_BOND
	EOR	#0FFH
	AND	!BP1 ;READ
	ORA	BP1_STATUS
	STA	BP1_STATUS
.ENDIF
.IF ( C_BP2_STATUS_EN .EQ. 1 )
	LDA	BP2_STATUS
	AND	#C_BP2_BOND
	STA	BP2_STATUS
	LDA	#C_BP2_BOND
	EOR	#0FFH
	AND	!BP2  ;READ
	ORA	BP2_STATUS
	STA	BP2_STATUS
.ENDIF
;
	RTS

