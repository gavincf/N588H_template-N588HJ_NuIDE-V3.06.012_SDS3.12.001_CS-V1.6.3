
BKX_IRTX_END_SOLVE:
	INC	IRTX_CNT
	LDA	IRTX_CNT
	CMP	#2
	BCS	BKX_IRTX_END_S10
;restart
	LDA	BP1_STATUS
	AND	#110B
	TAX
	LDA	IRTX_TBL0,X
	STA	IRTX_DLYCNT

	JMP	BKX_IRTX_END_SOLVE_RET
BKX_IRTX_END_S10:

	LDA	#C_BIT7
	TRB	IRTX_FLAG

	JMP	BKX_IRTX_END_SOLVE_RET

